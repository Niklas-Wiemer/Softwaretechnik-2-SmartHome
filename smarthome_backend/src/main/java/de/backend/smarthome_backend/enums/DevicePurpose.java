package de.backend.smarthome_backend.enums;

public enum DevicePurpose {

    /**

     Represents a consumer device.
     A consumer device is used for consuming or receiving Electricity or Water.
     It typically includes devices like WASHING_MACHINE, DISHWASHER, CAR, etc.
     */
    CONSUMER,
    /**

     Represents a producer device.
     A producer device is used for producing or generating Electricity.
     It typically includes devices like SOLAR_PANEL, etc.
     */
    PRODUCER
}
